package com.day1.Q1;

public class Coupling {

	public static void main(String[] args) {
		System.out.println("Tight coupling - When an object creates the object to be used, then it is a\n tight coupling situation. As the main object creates the object itself, this object can not be changed from outside world easily marked it as tightly coupled objects.");
		System.out.println("Loose coupling - When an object gets the object to be used from the outside, then it is a loose coupling situation. As the main object is merely using the object, this object can be changed from the outside world easily marked it as loosely coupled objects.");
	}

}
